# Demostración del método center()

print('[' + 'alfa'.center(10) + ']')

print('[' + 'Beta'.center(2) + ']')

print('[' + 'Beta'.center(4) + ']')

print('[' + 'Beta'.center(6) + ']')

# La variante de dos parámetros de center() hace uso del caracter del segundo argumento, en lugar de un espacio.

print('[' + 'gamma'.center(20, '*') + ']')