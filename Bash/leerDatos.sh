#!/bin/bash

# Pedir al usuario que introduzca varios valores
echo "Por favor, introduce tu nombre, edad y ciudad (separados por espacios):"

# Leer la entrada del usuario y almacenarla en las variables 'nombre', 'edad', y 'ciudad'
read nombre edad ciudad

# Mostrar los valores introducidos
echo "Nombre: $nombre"
echo "Edad: $edad"
echo "Ciudad: $ciudad"

